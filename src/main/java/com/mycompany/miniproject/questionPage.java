    private void optoionMarked()
    {
        if(rdboption1.isSelected())
        {
            stud.optionTicked[question_no - 1] = 1;
            lbloptionMarked.setText("Option A marked Successfully");
            System.out.println("option A marked");

        }
        else if(rdboption2.isSelected())
        {
            stud.optionTicked[question_no - 1] = 2;
            lbloptionMarked.setText("Option B marked Successfully");
            System.out.println("option b marked");
        }
        else if(rdboption3.isSelected())
        {
            stud.optionTicked[question_no - 1] = 3;
            lbloptionMarked.setText("Option C marked Successfully");
            System.out.println("option c marked");
        }
        else if(rdboption4.isSelected())
        {
            stud.optionTicked[question_no - 1] = 4;
            lbloptionMarked.setText("Option D marked Successfully");
            System.out.println("option d marked");
        }
        else
        {
            stud.optionTicked[question_no - 1] = -1;
            lbloptionMarked.setText("Option Unmarked Successfully");
        }
        btnColor();
        
        for(int i = 0; i < stud.optionTicked.length; i++)
        {
            System.out.print(stud.optionTicked[i] + "  ");
        }
        System.out.println();
        
    }


    private void displayQuestion(int question_no)
    {
        btnOnQuestion();
        
        try
        {
            
            MongoClient mongo = new MongoClient("localhost", 27017);
            
            DB db = mongo.getDB("BloodDonation");
            
            DBCollection table = db.getCollection("BloodBank");
            
            DBCursor cursor = table.find();
            DBObject disp = cursor.next();
            
            
            while (cursor.hasNext()) 
            {
                double id  = (Double)disp.get("_id");
                int idd = (int)id;
                System.out.println("Step 1");
                System.out.println(question_no);
                
                if(idd == question_no)
                {
                    String questionString = (String) disp.get("question");
                    String option_1String = (String) disp.get("option_1");
                    String option_2String = (String) disp.get("option_2");
                    String option_3String = (String) disp.get("option_3");
                    String option_4String = (String) disp.get("option_4");
                    
                    questionString = "<html><p>" + questionString + "</p></html>";
                
                    lblquestion.setText(questionString);
                    rdboption1.setText(option_1String);
                    rdboption2.setText(option_2String);
                    rdboption3.setText(option_3String);
                    rdboption4.setText(option_4String);
                    break;
        
                }
                else
                {
                    disp =  cursor.next();
                }
            }
            
              
        } 
        catch (HeadlessException | NumberFormatException  e)
        {
            JOptionPane.showMessageDialog(mainPanel, "Error = " + e);
        }
    }

